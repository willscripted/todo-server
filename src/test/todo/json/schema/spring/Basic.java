package todo.json.schema.spring;

/**
 * Basic class for use in tests of JSON schema related objects
 * @author Will O'Brien
 */
public class Basic {
    
    private int myInt;
    private String myString;


    public Basic() {
        
    }

    public int getMyInt() {
        return myInt;
    }

    public void setMyInt(int myInt) {
        this.myInt = myInt;
    }

    public String getMyString() {
        return myString;
    }

    public void setMyString(String myString) {
        this.myString = myString;
    }
}
